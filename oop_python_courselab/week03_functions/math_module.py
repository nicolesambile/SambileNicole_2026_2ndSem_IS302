def add(a_nbs, b_nbs):
    return a_nbs + b_nbs

def subtract(a_nbs, b_nbs):
    return a_nbs - b_nbs    

def multiply(a_nbs, b_nbs):
    return a_nbs * b_nbs

def divide(a_nbs, b_nbs):
    if b_nbs == 0:
        return "Cannot divide by zero"
    return a_nbs / b_nbs

